package vo;

public class Leave_historyVO {
    String idx, empno, total_leave, year, remain_leave;

    public String getIdx() {
        return idx;
    }

    public void setIdx(String idx) {
        this.idx = idx;
    }

    public String getEmpno() {
        return empno;
    }

    public void setEmpno(String empno) {
        this.empno = empno;
    }

    public String getTotal_leave() {
        return total_leave;
    }

    public void setTotal_leave(String total_leave) {
        this.total_leave = total_leave;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getRemain_leave() {
        return remain_leave;
    }

    public void setRemain_leave(String remain_leave) {
        this.remain_leave = remain_leave;
    }
}
